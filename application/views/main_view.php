<h2 class="blue">Система качества ISO</h2>

<p>В проектном институте ООО &ldquo;УрбаСтрой&raquo; внедрена и поддерживается система менеджмента качества (СМК) <br>
  применительно к работам, которые оказывают влияние на безопасность строительства. Соответствие СМК требованиям <br>
  ГОСТ Р ИСО (ISO 9001-2011) удостоверено сертификатом, выданным 18.11.2013 по системе добровольной сертификации.</p>
<p>Область действия СМК института: инженерные изыскания, разработка проектной и рабочей документации на строительство, <br>
  реконструкцию, ремонт автомобильных и железных дорог и их инфраструктуры, искусственных сооружений, объектов <br>
  промышленного и гражданского строительства, осуществление функций генерального проектировщика и генерального <br>
  подрядчика на проведение инженерных изысканий.</p>
<p>В систему менеджмента качества ООО &ldquo;УрбаСтрой&rdquo; заложены следующие основные принципы:</p>
<p>ориентация на потребителя: институт зависит от своих потребителей и поэтому должен понимать их текущие и будущие <br>
  потребности, выполнять их требования и стремиться превзойти их ожидания;<br>
  процессный подход: желаемый результат достигается эффективнее, когда деятельностью и соответствующими ресурсами <br>
  управляют как процессом, в том числе при движении к целям в области качества;<br>
  системный подход к менеджменту: выявление, понимание и менеджмент взаимосвязанных процессов как системы содействуют <br>
  повышению результативности и эффективности организации при достижении ее целей;<br>
  постоянное улучшение: постоянное улучшение качества разрабатываемых проектов и предоставляемых институтом услуг в <br>
  целом следует рассматривать как неизменную цель;<br>
  принятие решений, основанное на фактах: эффективные решения должны основываться на анализе данных и информации;<br>
  взаимовыгодные отношения с субподрядными организациями: институт и его субподрядные организации взаимозависимы, <br>
  поэтому отношения взаимной выгоды повышают способность обеих сторон создавать ценности.</p>
