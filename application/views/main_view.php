         <h2> О компании</h2>
        
<p>Добро пожаловать на страницу нашей компании. </p>
<p>История компании ООО &ldquo;УрбаСтрой&rdquo; началась в 2008 году, в период стремительного выхода  из мирового финансового кризиса<br>
  и развития строительного комплекса Санкт-Петербурга  и Ленинградской области.</p>
<p>Накопив положительный опыт, ООО &ldquo;Урбастрой&rdquo; в настоящее время выполняет весь комплекс работ от проектирования и <br>
  строительства объекта до ввода его в эксплуатацию.</p>
<p>Благодаря нашему опыту и высококвалифицированному персоналу, ООО &ldquo;УрбаСтрой&rdquo; готово выполнить проектные и строительные <br>
  работы, реконструкцию и капитальный ремонт следующих объектов: автодорог и примыкающих к ним объектов, зданий и <br>
  сооружений, консервацию зданий и сооружений,  а также выполнить отделочные работы (как внутренние, так и внешние), а также <br>
  ремонт помещений.</p>
<p>Мы с большим удовольствием реализовываем самые уникальные и сложные объекты, воплощая в реальность любые, даже <br>
  самые смелые идеи наших заказчиков. </p>
<p>Для поддержания качества наших работ, мы постоянно повышаем квалификацию наших сотрудников и применяем <br>
  cовременные технологии при выполнении наших работ. </p>
<p>Именно поэтому девиз нашей компании -  <b>Путь к успеху лежит через самосовершенство!</b></p>
